public class Code02_07 {

    public static void main(String[] args) {

        int a, b, c, re;
        a = 100;
        b = 50;

        re = a + b;
        System.out.println(a + "+" + b + "=" + re);

        re = a - b;
        System.out.println(a + "-" + b + "=" + re);

        re = a * b;
        System.out.println(a + "*" + b + "=" + re);

        re = a / b;
        System.out.println(a + "/" + b + "=" + re);
    }
}
